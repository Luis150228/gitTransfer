  `dateEnd` datetime NOT NULL,
  `numMonth` varchar(2) DEFAULT NULL,
  `month` varchar(12) DEFAULT NULL,
  `range` varchar(17) DEFAULT NULL,
  PRIMARY KEY (`id`)
